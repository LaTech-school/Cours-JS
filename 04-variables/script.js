// Les variables

// -> Un espace mémoire (dans la RAM)
// -> prend la taille de ce qu'il contient
// -> sera de type de ce qu'il contient

// Syntaxe d'un nom de variable
// -> ☑ Caractères alphabétique et caractères numérique
// -> ☑ Majuscules / Minuscules
// -> ☑ Tiret du 8 (underscore en anglais) "_"
// -> ☒ Pas de caractères spéciaux (+:/;.#@&é"'(§è!çà)° ...)
// -> ☒ Ne commence pas par un numérique
// -> ☒ Tiret du 6 (dash en anglais) "-"
var azertyu12345;
var AzerTY;
var azerty;
var _1azerty;
var a_b;
var azertyu________;

// Ecritue slug / snake
var user_name;

// Lower Camel Case
var userName;

// Upper Camel Case
var UserName;


// On déclare une variable avec le mot clé "var"
// Suivi d'un nom de variable (ex: la variable "name")
var name;

// Affectation d'une valeur, utilisation de l'opérateur d'affection "simple egal"
// (ex: name prend la valeur de "Bob")
var name = "Bob";

// Les variables déclarées par "var" ont une portée globale à l'application

// Les variables déclarées par "let" ont une portée locale dans l'application

let age = 2;


// Voir le contenu / Appel de la variable
