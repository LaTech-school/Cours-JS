// Boucle : While
// Execute du code tant que la condition est vérifiée

// Initialisation de bouclage
var i = 0;

while (i < 10) // condition de bouclage
{
    console.log(i);

    // Incrémentation de boucle
    // ++i;
    i = i + 1;
}

