// Les Opérateurs

// Les opérateurs d'affectation
// --

// Affectation simple 
// Symbole:     =
// Syntaxe:     a = b 
var a = 42;



// Les opérateurs de comparaison
// --

// Egalité
// Symbole:     ==
// Syntaxe:     a == b
console.log( '42 == "42"', 42 == "42" );

// Inegalite
// Symbole:     !=
// Syntaxe:     a != b
console.log( '42 != "42"', 42 != "42" );
console.log( '40 != "42"', 40 != "42" );
console.log( '40 != 42', 40 != 42 );

// Egalité strict
// Symbole:     ===
// Syntaxe:     a === b
console.log( '42 === "42"', 42 === "42" );

// Inégalité strict
// Symbole:     !==
// Syntaxe:     a !== b
console.log( '42 !== "42"', 42 !== "42" );