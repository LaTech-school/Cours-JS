// Les sorties JS


// Sortie en boite de dialogue
// --

// Boite de dialogue de type ALERT
// Utilisée pour afficher un  message en boite de dialogue
alert("Hello World");

// Boite de dialogue de type PROMPT
// Utilisée pour demander une information à l'utilisateur
prompt("Write your name");

// Boite de dialogue de type CONFIRM
// Utilisée pour demander une confirmation à l'utilisateur
confirm("continue ?");


// Sorties en Console
// --

// Sortie dans la console : Type LOG
// Utilisée pour les messages d'èvènements et débug
console.log("Hello World");

// Sortie dans la console : Type INFO
// Utilisée pour les messages d'info
console.info("Hello World");

// Sortie dans la console : Type WARNING
// Utilisée pour les messages d'erreur simple
console.warn("Hello World");

// Sortie dans la console : Type DANGER
// Utilisée pour les messages d'erreur fatale
console.danger("GoodBye World");
